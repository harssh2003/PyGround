countries_list = list(countries_data.keys())

import tkinter as tk
from tkinter import ttk
import random
from PIL import Image, ImageTk
from tkinter import Label, Button, Toplevel


class GameWindow(tk.Toplevel):
    def __init__(self, parent):
        super().__init__(parent)
        self.parent = parent
        self.title("Guess The Capital")
        screen_width = self.winfo_screenwidth()
        screen_height = self.winfo_screenheight()

        # Set the width and height of the window
        window_width = 500
        window_height = 500

        # Calculate the x and y coordinates for the window to be centered
        x_coordinate = (screen_width - window_width) // 2
        y_coordinate = (screen_height - window_height) // 2

        # Set the geometry of the window
        self.geometry(f"{window_width}x{window_height}+{x_coordinate}+{y_coordinate}")

        self.configure(bg="#9EDDFF")
        self.resizable(False, False)

        style = ttk.Style()
        style.theme_use('alt')
        style.configure('TLabel', font=('Cambria', 20), background="#9EDDFF")
        style.configure('Bootstrap.TButton', font=('Cambria', 16), background="#6499E9", foreground="black")

        self.score = 0
        self.countries_asked = []
        self.next_question = self.get_next_question()

        self.next_question_label = ttk.Label(self, text=f"The capital of {self.next_question} is:")
        self.next_question_label.pack(pady=20)

        self.answer_entry = ttk.Entry(self, font=('Cambria', 14))
        self.answer_entry.pack(pady=10)

        self.check_button = ttk.Button(self, text="Check", command=self.check_answer, style='Bootstrap.TButton')
        self.check_button.pack(pady=10)

        self.result_label = ttk.Label(self, text="", font=('Cambria', 16), background="#9EDDFF")
        self.result_label.pack(pady=20)

        self.correct_image = Image.open("correctPanda.png")
        self.correct_image = self.correct_image.resize((200, 200))
        self.correct_image = ImageTk.PhotoImage(self.correct_image)

        self.wrong_image = Image.open("wrongPanda.png")
        self.wrong_image = self.wrong_image.resize((200, 200))
        self.wrong_image = ImageTk.PhotoImage(self.wrong_image)

        self.image_label = ttk.Label(self, image=None)
        self.image_label.pack(pady=20)

    def check_answer(self):
        answer = self.answer_entry.get().strip().lower()
        expected_answer = countries_data[self.next_question].lower()

        if expected_answer != answer:
            self.correct_answer = expected_answer.upper()
            self.result_label.config(text=f"The correct answer is {self.correct_answer}!\nGame Over! Score: {self.score}", justify="center")
            self.check_button.config(text="Go to Home Page", command=self.game_over, style='Bootstrap.TButton')
            self.image_label.config(image=self.wrong_image)
        else:
            self.score += 1
            self.result_label.config(text=f"You Got That Correct!  Score = {self.score}")
            self.countries_asked.append(self.next_question)
            self.next_question = self.get_next_question()
            self.next_question_label.config(text=f"The capital of {self.next_question} is:")
            self.answer_entry.delete(0, tk.END)

            # Display the image
            self.image_label.config(image=self.correct_image)
            # self.image_label.image = self.correct_image  # Keep a reference to avoid garbage collection

    def game_over(self):
        self.destroy()  # Close the game window
        self.parent.deiconify()  # Show the main window

    def get_next_question(self):
        next_question = random.choice(countries_list)
        while next_question in self.countries_asked:
            next_question = random.choice(countries_list)
        return next_question


class GameApp(tk.Tk):
    def __init__(self):
        super().__init__()
        self.title("Country Capitals Game")
        screen_width = self.winfo_screenwidth()
        screen_height = self.winfo_screenheight()

        # Set the width and height of the window
        window_width = 500
        window_height = 500

        # Calculate the x and y coordinates for the window to be centered
        x_coordinate = (screen_width - window_width) // 2
        y_coordinate = (screen_height - window_height) // 2

        # Set the geometry of the window
        self.geometry(f"{window_width}x{window_height}+{x_coordinate}+{y_coordinate}")

        self.configure(bg="#9EDDFF")
        self.resizable(False, False)

        self.panda_image = Image.open("panda.png")
        self.panda_image = self.panda_image.resize((200, 200))
        self.panda_image = ImageTk.PhotoImage(self.panda_image)

        self.image_label = ttk.Label(self, image=None)
        self.image_label.pack(pady=20)

        style = ttk.Style()
        style.theme_use('alt')
        style.configure('TLabel', font=('Cambria', 20), background="#C4E7ED")
        style.configure('Bootstrap.TButton', font=('Cambria', 16), background="#6499E9", foreground="black")

        # Use the custom style for buttons
        self.start_button = ttk.Button(self, text="Start a New Game", style='Bootstrap.TButton',command=self.start_new_game)
        self.start_button.pack(pady=20)

        self.quit_button = ttk.Button(self, text="Quit", style='Bootstrap.TButton', command=self.quit)
        self.quit_button.pack(pady=20)
        self.image_label.config(image=self.panda_image)

    def start_new_game(self):
        self.withdraw()  # Hide the main window
        self.game_window = GameWindow(self)


def StartTheGame():
    if __name__ == "__main__":
        app = GameApp()
        app.mainloop()
        app.resizable(False, False)


StartTheGame()
